    // OPERACAO UPDATE
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        Autor autor = Services.read(conn, "autores", "id", textoId.getText());
        
        Services.update(conn, autor, "nome", textoNome.getText());
        Services.update(conn, autor, "livros", textoLivros.getText());
        Services.update(conn, autor, "data_nascimento", textoDataNascimento.getText());
    }//GEN-LAST:event_jButton2ActionPerformed

