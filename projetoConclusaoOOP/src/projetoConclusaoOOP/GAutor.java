    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        String nome = textoNome.getText();
        String livros = textoLivros.getText();
        LocalDate dataNascimento = LocalDate.parse(textoDataNascimento.getText());
        
        Autor autor = new Autor(nome, livros, dataNascimento.toString());
        Services.create(conn, autor);
    }//GEN-LAST:event_jButton1ActionPerformed

    // OPERACAO READ
    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:

        try {
            Autor autor = Services.read(conn, "autores", "nome", textoNome.getText());
            textoId.setText(Integer.toString(Autor.getId(textoNome.getText())));
            textoLivros.setText(autor.getLivros());
            textoDataNascimento.setText(autor.getDataNascimento().toString());
        
        } catch(Exception ex) {
            System.out.println("Erro: campo nao existe ou possivelmente ja foi excluido");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

